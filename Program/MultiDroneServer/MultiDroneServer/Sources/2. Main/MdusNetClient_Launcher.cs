using UnityEngine;
using System;
using System.Collections;
using System.Collections.Generic;
using J2y.Network;

namespace J2y.MultiDroneUnity
{
    //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    //
    // MdusNetClient_Launcher
    //
    //
    //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

    public class MdusNetClient_Launcher : JObject
    {
        //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
        // 변수
        //
        //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

        #region [변수] Base
        public NetPeer_base NetPeer { get; set; }
        public MdNetData_Launcher NetData { get; set; }
        #endregion

        //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
        // 메인 함수
        //
        //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

        #region [초기화] 생성자
        //------------------------------------------------------------------------------------------------------------------------------------------------------
        public override void Awake()
        {
            base.Awake();

            NetData = new MdNetData_Launcher();
            NetData._guid = _guid;
            MdusDataCenter.NetClient_Launchers.Add(this);
        }
        #endregion

        //#region [업데이트] Update    
        ////------------------------------------------------------------------------------------------------------------------------------------------------------
        //public void FixedUpdate()
        //{
        //    _movement.Update(true);
        //}
        //#endregion


        #region [종료] 삭제
        //------------------------------------------------------------------------------------------------------------------------------------------------------
        public override void OnDestroy()
        {
            MdusDataCenter.NetClient_Launchers.Remove(this);
        }
        #endregion





        //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
        // 네트워크
        //
        //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

        #region [네트워크] 전송
        //------------------------------------------------------------------------------------------------------------------------------------------------------
        public override eNetSendResult NetSendMessage(JNetMessage message)
        {
            return NetPeer.SendMessage(message);
        }

        public override void NetBroadcast(JNetMessage message, NetPeer_base except = null)
        {
            NetPeer.Broadcast(message);
        }
        #endregion



    }

}
